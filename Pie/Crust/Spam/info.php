					<h2>What is Fake Follower Check?</h2>
					<p>Fake Follower Check does what it says. It checks your Twitter followers to see if they are real or fake. Or in other words, it checks to see how many spam accounts are following you. And the great thing about it is you can check your friends and 'enemies' too...</p>
					<h2>Why did you create it?</h2>
					<p>After reading the Kernel Mag's posts on the subject of <a href="http://www.kernelmag.com/yiannopoulos/2736/ive-been-mazied/" target="_blank">fake followers</a> we thought it would be a nice idea to create a tool that allows you to check how many fake followers you and your friends have.</p>
					<h2>How does it work?</h2>
					<p>We take a sample of your follower data. Up to 1,000 records depending on how 'popular' you are and assess them against a number of simple spam criteria.</p>
					<p>On a very basic level spam accounts tend to have few or no followers and few or no tweets. But in contrast they tend to follow a lot of other accounts.</p> 
					<h2>How accurate is it?</h2>
					<p>For those of you with 50,000* followers or less we believe our tool will provide a very accurate insight into how many inactive and fake followers you have.</p>
					<p>If you're very, very 'popular' the tool will still provide good insight but may better reflect your current follower activity rather than your whole follower base.</p>
					<h2>Why is it important?</h2>
					<p>There are two reasons. First it's important for you to be sure when you communicate on Twitter that you are communicating with real and active followers. Because the more active your follower base the more likely they are to share your content.</p>
					<p>The second reason is there are a growing number of Fakers out there. People who buy followers in a vain attempt to build legitimacy. "Look at me I have 20,000 followers, I must know my..." They are essentially trying to game the system and it's important for you to be able to spot them, and steer clear of them. Because ultimately if you're willing to lie about how many friends you have you are not a very trustworthy individual.</p>
